wordSearch.addEventListener("input", () => {
    const keyboard = wordSearch.value.trim();

    const filteredWords = words.filter(word => {
        return(
            word.term.includes(keyboard)  ||
            word.kana.includes(keyboard)  ||
            word.meaning.includes(keyboard)
        );
    });

    showWords(sortWords(filteredWords));
});

function filterKana(group){
    if(group === "all"){
        showWords(sortWords(words));
        return;
    }

    const filtered = words.filter(word => {
        const first = word.kana.charAt(0);

        if(group === "あ") return "あいうえお".includes(first);
        if(group === "か") return "かきくけこがぎぐげご".includes(first);
        if(group === "さ") return "さしすせそざじずぜぞ".includes(first);
        if(group === "た") return "たちつてとだぢづでど".includes(first);
        if(group === "な") return "なにぬねの".includes(first);
        if(group === "は") return "はひふへほばびぶべぼぱぴぷぺぽ".includes(first);
        if(group === "ま") return "まみむめも".includes(first);
        if(group === "や") return "やゆよ".includes(first);
        if(group === "ら") return "らりるれろ".includes(first);
        if(group === "わ") return "わをん".includes(first);
    });

    showWords(sortWords(filtered));
}

const kanaButtons = document.querySelectorAll(".kana-filter button");

kanaButtons.forEach(button => {

    button.addEventListener("click", () => {

        // 一旦全部のactiveを消す
        kanaButtons.forEach(btn => {
            btn.classList.remove("active");
        });

        // 押したボタンだけactiveを付ける
        button.classList.add("active");

        const group = button.dataset.group;
        filterKana(group);

    });

});

