const wordList = document.getElementById("wordList");
const wordSearch = document.getElementById("wordSearch");

function showWords(list) {
    wordList.innerHTML = "";

    list.forEach(word => {
        const card =document.createElement("div");
        card.className = word.nojima ? "word-card nojima" : "word-card";

        card.innerHTML = `
    <h3>
        ${word.term} 
        <span>(${word.kana})</span>
        ${word.nojima ? `<span class="page">${word.page}</span>` : ""}
    </h3>
    <p>${word.meaning}</p>
    `;

    wordList.appendChild(card);
    });
}


function sortWords(list) {
    return list.sort((a,b) => a.kana.localeCompare(b.kana, "ja"));
}

